void ReadTemp()
{
  int16_t temp16;
  HAL_UART_Receive(&huart2, temp_b, sizeof(temp_b), 1);
  temp16 = (temp_b[0] << 8) | temp_b[1];
  temp16 >>=3;
  if(temp16 & 0x1000){
  		temp_s1 = (temp16 - 8192)/16.0;
  	}else{
  		temp_s1 = temp16 / 16.0;
  	}
}

